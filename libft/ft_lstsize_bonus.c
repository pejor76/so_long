#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		cont;

	cont = 0;
	if (lst != NULL)
	{
		cont++;
		while (lst->next != NULL)
		{
			lst = lst->next;
			cont++;
		}
	}
	return (cont);
}
